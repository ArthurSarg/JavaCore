package classwork.lesson8;

public class MonthsExample {
    public static void main(String[] args) {
        int num = 9;
        switch (num) {
            case 1 :
                System.out.println("Հունվար");
                break;
            case 2 :
                System.out.println("Փետրվար");
                break;
            case 3 :
                System.out.println("Մարտ");
                break;
            case 4 :
                System.out.println("Ապրիլ");
                break;
            case 5 :
                System.out.println("Մայիս");
                break;
            case 6 :
                System.out.println("Հունիս");
                break;
            case 7 :
                System.out.println("Հւլիս");
                break;
            case 8 :
                System.out.println("Օգոստոս");
                break;
            case 9 :
                System.out.println("Սեպտեմբեր");
                break;
            case 10 :
                System.out.println("Հոկտեմբեր");
                break;
            case 11 :
                System.out.println("Նոյեմբեր");
                break;
            case 12 :
                System.out.println("Դեքտեմբեր");
                break;

        }
    }
}
